//! pass-mgr-ffi — the UniFFI boundary that lets the Compose Multiplatform mobile
//! app drive the audited [`pass_mgr_core`] vault without reimplementing any
//! crypto, storage, or recovery logic.
//!
//! # What this crate is
//!
//! A *thin, additive* wrapper. The security-critical core (two-password Argon2id
//! KDF, XChaCha20-Poly1305, the partitioned crash-safe volume/manifest format,
//! and the corruption-fails-closed recovery) lives in `pass-mgr-core` and is
//! reused **verbatim**. This crate only:
//!   * holds an opaque, mutex-guarded [`OpenVault`] behind a UniFFI interface,
//!   * exposes flat value DTOs (Kotlin/Swift data classes) for the five record
//!     types, and
//!   * maps the rich [`CoreVaultError`] down to a small, stable, **no-leak**
//!     [`VaultError`] (wrong-password and corrupt both collapse to one variant,
//!     exactly like the desktop's generic "can't open" message).
//!
//! # v1 scope — READ-ONLY
//!
//! Open with the two passwords, browse the five tabs, view a record, read its
//! edit history. No create/edit/delete/documents/rekey yet (those are additive
//! follow-ups). A read-only open writes nothing to disk and never takes the
//! single-writer lock.
//!
//! # Security downgrade (disclosed)
//!
//! Returning a record DTO copies its fields — including a cleartext password —
//! into the JVM/Swift heap, where `ZeroizeOnDrop` cannot reach and the GC/ARC
//! may duplicate the bytes. This is an inherent, documented downgrade versus the
//! desktop. The host must minimise copies, fetch history explicitly (it can
//! contain old/new passwords), and clear clipboard/fields itself.

// NOTE: this crate intentionally does NOT `#![forbid(unsafe_code)]` — the UniFFI
// scaffolding generated by `setup_scaffolding!()` contains `unsafe`. The core,
// which holds all the real logic, keeps its crate-wide `forbid`.

use std::path::PathBuf;
use std::sync::{Arc, Mutex};

use pass_mgr_core::records::{self, Record};
use pass_mgr_core::vault::{OpenVault, VaultError as CoreVaultError};

uniffi::setup_scaffolding!();

// ---------------------------------------------------------------------------
// Error: a small, stable, no-leak surface mapped from CoreVaultError.
// ---------------------------------------------------------------------------

/// Stable error surface for the mobile host. Crucially, a wrong password and a
/// corrupt/tampered vault collapse to the SAME [`VaultError::WrongPasswordOrCorrupt`]
/// variant with no detail, so the FFI never becomes a correct-password oracle —
/// mirroring the desktop's single generic failure message.
#[derive(Debug, thiserror::Error, uniffi::Error)]
pub enum VaultError {
    /// The two passwords were wrong, OR the vault file is corrupt/tampered. These
    /// are deliberately indistinguishable (no leak). Re-check both passwords and
    /// their order first.
    #[error("wrong passwords, or the vault is corrupt")]
    WrongPasswordOrCorrupt,
    /// No vault file exists at the given location.
    #[error("no vault found at the given location")]
    NotFound,
    /// An interrupted password change is pending; it must be finished on a
    /// writable (desktop) open before the vault can be read here.
    #[error("an interrupted password change is pending; finish it on desktop (--write)")]
    RekeyPending,
    /// A document referenced by the vault is missing from the store — possible
    /// tampering or a rollback to an older snapshot.
    #[error("a referenced document is missing (possible tampering or rollback)")]
    ArchiveMismatch,
    /// Another writable session holds the vault (should not occur for read-only).
    #[error("the vault is open in another session")]
    Locked,
    /// No record with the requested id exists in that tab.
    #[error("record not found")]
    RecordNotFound,
    /// A low-level I/O error touching the vault directory.
    #[error("I/O error")]
    Io,
    /// An unexpected internal error.
    #[error("internal error")]
    Internal,
}

impl From<CoreVaultError> for VaultError {
    fn from(e: CoreVaultError) -> Self {
        match e {
            CoreVaultError::NotFound(_) => VaultError::NotFound,
            CoreVaultError::RekeyPending => VaultError::RekeyPending,
            CoreVaultError::ArchiveMismatch => VaultError::ArchiveMismatch,
            CoreVaultError::Locked => VaultError::Locked,
            CoreVaultError::Io(_) => VaultError::Io,
            // Everything that could discriminate a wrong password from a damaged
            // vault — the AEAD decrypt failure, bad magic/version/params, a
            // truncated file, a JSON/storage decode failure — collapses to ONE
            // generic variant so there is no correct-password oracle.
            CoreVaultError::Crypto(_)
            | CoreVaultError::BadMagic
            | CoreVaultError::BadVersion(_)
            | CoreVaultError::Truncated
            | CoreVaultError::BadParams
            | CoreVaultError::Json(_)
            | CoreVaultError::Storage(_) => VaultError::WrongPasswordOrCorrupt,
            // Not reachable on a read-only open, but map defensively.
            CoreVaultError::AlreadyExists(_)
            | CoreVaultError::ReadOnly
            | CoreVaultError::TooLarge
            | CoreVaultError::NoSuchPartition(_) => VaultError::Internal,
        }
    }
}

