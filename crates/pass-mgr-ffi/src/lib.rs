/// An unlocked vault. Opaque to the host: the derived key never crosses the FFI.
/// Wraps the core [`OpenVault`] behind a mutex so the host can hold exactly one
/// handle and every call is serialised (this stands in for the desktop's
/// cross-process single-writer file lock, which a single app process doesn't
/// need). Dropping the handle runs the core's zeroize-on-drop.
#[derive(uniffi::Object)]
pub struct Vault {
    inner: Mutex<OpenVault>,
}

/// Open an existing vault **read-only** with the two passwords, in order.
///
/// `dir` is the app-private directory that holds `vault.pmv` (+ `manifest/`,
/// `volume/`) — the host hands in its sandbox path; arbitrary user paths are not
/// accepted. `pw1`/`pw2` are the raw password bytes. UniFFI lowers the host's
/// ByteArray/Data into Rust-OWNED `Vec<u8>` copies, so this function zeroizes them
/// before returning (on both success and failure); the host should still overwrite
/// its own copies afterwards.
#[uniffi::export]
pub fn open_vault(dir: String, mut pw1: Vec<u8>, mut pw2: Vec<u8>) -> Result<Arc<Vault>, VaultError> {
    let path = PathBuf::from(dir).join("vault.pmv");
    let opened = OpenVault::open_read_only(path, &pw1, &pw2);
    // Wipe our owned plaintext copies before they are freed, so password bytes do
    // not linger in the heap after open returns (the desktop binary is equally
    // careful via Zeroizing). Done on the error path too.
    pw1.zeroize();
    pw2.zeroize();
    Ok(Arc::new(Vault { inner: Mutex::new(opened?) }))
}

#[uniffi::export]
impl Vault {
    /// Human-readable notice if the live `vault.pmv` was unreadable and the vault
    /// was recovered from an in-place redundant copy (a roll-forward/rollback);
    /// `None` on a normal open. Surface it so the user is rollback-aware.
    pub fn recovery_notice(&self) -> Option<String> {
        self.lock().recovery_notice().map(|s| s.to_string())
    }

    /// The monotonic write counter of the snapshot just opened. A surprise drop
    /// versus a remembered value signals a whole-file rollback to an older copy.
    pub fn generation(&self) -> u64 {
        self.lock().opened_generation()
    }

    /// Unix-seconds timestamp the vault was previously opened (0 if never).
    pub fn previous_access(&self) -> i64 {
        self.lock().previous_access()
    }

    /// The master list for one tab: `{id, label}` per record, in stored order.
    pub fn list_records(&self, kind: RecordKind) -> Vec<RecordSummary> {
        let ov = self.lock();
        let v = &ov.vault;
        match kind {
            RecordKind::Instruction => v.instructions.iter().map(summary).collect(),
            RecordKind::TrustWill => v.trust_wills.iter().map(summary).collect(),
            RecordKind::AssetLiability => v.assets.iter().map(summary).collect(),
            RecordKind::Account => v.accounts.iter().map(summary).collect(),
            RecordKind::RealEstate => v.real_estate.iter().map(summary).collect(),
        }
    }

    /// How many records are in a tab (cheap, no cleartext crosses the boundary).
    pub fn count(&self, kind: RecordKind) -> u32 {
        let ov = self.lock();
        let v = &ov.vault;
        let n = match kind {
            RecordKind::Instruction => v.instructions.len(),
            RecordKind::TrustWill => v.trust_wills.len(),
            RecordKind::AssetLiability => v.assets.len(),
            RecordKind::Account => v.accounts.len(),
            RecordKind::RealEstate => v.real_estate.len(),
        };
        n as u32
    }

    pub fn get_instruction(&self, id: String) -> Result<Instruction, VaultError> {
        let ov = self.lock();
        ov.vault
            .instructions
            .iter()
            .find(|r| r.id == id)
            .map(map_instruction)
            .ok_or(VaultError::RecordNotFound)
    }

    pub fn get_trust_will(&self, id: String) -> Result<TrustWill, VaultError> {
        let ov = self.lock();
        ov.vault
            .trust_wills
            .iter()
            .find(|r| r.id == id)
            .map(map_trust_will)
            .ok_or(VaultError::RecordNotFound)
    }

    pub fn get_asset(&self, id: String) -> Result<AssetLiability, VaultError> {
        let ov = self.lock();
        ov.vault
            .assets
            .iter()
            .find(|r| r.id == id)
            .map(map_asset)
            .ok_or(VaultError::RecordNotFound)
    }

    pub fn get_account(&self, id: String) -> Result<Account, VaultError> {
        let ov = self.lock();
        ov.vault
            .accounts
            .iter()
            .find(|r| r.id == id)
            .map(map_account)
            .ok_or(VaultError::RecordNotFound)
    }

    pub fn get_real_estate(&self, id: String) -> Result<RealEstate, VaultError> {
        let ov = self.lock();
        ov.vault
            .real_estate
            .iter()
            .find(|r| r.id == id)
            .map(map_real_estate)
            .ok_or(VaultError::RecordNotFound)
    }

    /// A record's edit history, newest concerns last. Fetched explicitly (never
    /// bundled into the record DTO) because `detail` can contain cleartext
    /// old/new passwords — the host can gate or redact it.
    pub fn get_history(&self, kind: RecordKind, id: String) -> Result<Vec<Change>, VaultError> {
        let ov = self.lock();
        let v = &ov.vault;
        let history = match kind {
            RecordKind::Instruction => v.instructions.iter().find(|r| r.id == id).map(|r| &r.history),
            RecordKind::TrustWill => v.trust_wills.iter().find(|r| r.id == id).map(|r| &r.history),
            RecordKind::AssetLiability => v.assets.iter().find(|r| r.id == id).map(|r| &r.history),
            RecordKind::Account => v.accounts.iter().find(|r| r.id == id).map(|r| &r.history),
            RecordKind::RealEstate => v.real_estate.iter().find(|r| r.id == id).map(|r| &r.history),
        };
        history
            .map(|h| h.iter().map(map_change).collect())
            .ok_or(VaultError::RecordNotFound)
    }

    /// The vault-wide audit log (created/deleted/opened/compacted events).
    pub fn audit_log(&self) -> Vec<Change> {
        self.lock().vault.audit.iter().map(map_change).collect()
    }
}

impl Vault {
    /// Lock the inner handle, recovering from a poisoned mutex (a panic in a
    /// previous call) by taking the guard anyway — the core data is still valid
    /// and there is only one handle, so there is nothing to corrupt.
    fn lock(&self) -> std::sync::MutexGuard<'_, OpenVault> {
        self.inner.lock().unwrap_or_else(|p| p.into_inner())
    }
}

fn summary<R: Record>(r: &R) -> RecordSummary {
    RecordSummary { id: r.id().to_string(), label: r.label() }
}

