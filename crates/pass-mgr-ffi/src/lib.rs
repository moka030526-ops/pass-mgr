/// Which of the five tabs / record collections to operate on.
#[derive(Debug, Clone, Copy, uniffi::Enum)]
pub enum RecordKind {
    Instruction,
    TrustWill,
    AssetLiability,
    Account,
    RealEstate,
}

/// One row in a tab's master list.
#[derive(uniffi::Record)]
pub struct RecordSummary {
    pub id: String,
    /// Human label (same logic the desktop list uses).
    pub label: String,
}

/// One timestamped history / audit entry. `detail` MAY contain cleartext old/new
/// field values (including passwords) by design — gate/redact on the host.
#[derive(uniffi::Record)]
pub struct Change {
    pub at: i64,
    pub action: String,
    pub detail: String,
}

#[derive(uniffi::Record)]
pub struct Instruction {
    pub id: String,
    pub title: String,
    pub description: String,
    pub created_at: i64,
    pub updated_at: i64,
}

#[derive(uniffi::Record)]
pub struct TrustWill {
    pub id: String,
    pub document: String,
    pub usage: String,
    /// Volume file id of an attached document, if any (opening it is post-MVP).
    pub file: Option<String>,
    pub created_at: i64,
    pub updated_at: i64,
}

#[derive(uniffi::Record)]
pub struct AssetLiability {
    pub id: String,
    pub kind: String,
    pub description: String,
    pub owner: String,
    pub approx_value: String,
    pub as_of_date: String,
    pub institution: String,
    pub asset_type: String,
    pub url: String,
    pub beneficiary: String,
    pub review: bool,
    pub statement: Option<String>,
    pub created_at: i64,
    pub updated_at: i64,
}

#[derive(uniffi::Record)]
pub struct Account {
    pub id: String,
    pub account_type: String,
    pub account_subtype: String,
    pub owner: String,
    pub username: String,
    /// Cleartext. Crosses into the JVM/Swift heap — see the crate-level note.
    pub password: String,
    pub description: String,
    pub url: String,
    pub review: bool,
    pub created_at: i64,
    pub updated_at: i64,
}

#[derive(uniffi::Record)]
pub struct RealEstate {
    pub id: String,
    pub address: String,
    pub ownership: String,
    pub taxes: String,
    pub hoa: String,
    pub income_account: String,
    pub financing_account: String,
    pub payment_account: String,
    pub created_at: i64,
    pub updated_at: i64,
}

